				<h3>Description:</h3>
				
				<p>Aiming for this app to help you figure out what you can make with whatever spirits you have. 
				For example if you have a bottle of gin it will let you know the most common drinks people make with fun and the ingredients needed. 
				You can check off potentially obtainable ingredients or exclude ingredients you definitely don't want. 
				Hopefully it will be more that a "what can I make last minute drink" app, 
				but rather a "I have all these ingredients let's see what I can make" app.</p>
				
				
