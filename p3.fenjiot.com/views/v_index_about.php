		<h2>Project 3 -- title: Dmg</h2>
		<div>
			<h3>Description:</h3>
			
			<p class="crossthat">Aiming for this app to help you figure out what you can make with whatever spirits you have. 
			For example if you have a bottle of gin it will let you know the most common drinks people make with fun and the ingredients needed. 
			You can check off potentially obtainable ingredients or exclude ingredients you definitely don't want. 
			Hopefully it will be more that a "what can I make last minute drink" app, 
			but rather a "I have all these ingredients let's see what I can make" app.</p>
			<br>
			<p> -- UPDATED app description -- </p>
			<br>
			<p>Changed app to allow you to specify:
				<ul>
					<li>1) how many people are drinking</li>
					<li>2) how many drinks they are having</li>
					<li>3) how many shots they are having</li>
				</ul>
			</p>
			<br>
			<p>The idea was that users would be able to input the information listed above and submit it. 
			Users would then provide additional information regarding their drinks of choice to calculate how much of each ingredient would be needed.
			For this to work, a database of drink mixes and recipes needs to be hooked up still.
			</p>
				
